function encontrarNumerosIguais(array1, array2) {
    const numerosIguais = [];

    // Iterar sobre cada número do primeiro array
    for (const numero of array1) {
        // Verificar se o número está presente no segundo array
        if (array2.includes(numero)) {
            // Se estiver, adicionar ao array de números iguais
            numerosIguais.push(numero);
        }
    }

    return numerosIguais;
}

